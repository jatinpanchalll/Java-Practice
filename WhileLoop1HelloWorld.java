 // WHILE LOOP
 // PRINT HELLO WORLD IN TIMES INPUT
 import java.util.*;
 public class WhileLoop1HelloWorld {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        
        int Time = sc.nextInt();
        int Counter = 0;

       while(Counter < Time){
        System.out.println("Hello World");
        Counter ++;
       }
       
    }
    }
