public class BitWiseOperators{
    public static void main(String[] args) {
        
        //BINERY AND & OPERATOR
        System.out.println(5&6);

        //BINERY OR | OPERATOR
        System.out.println(5|6);

        //BINERY XOR ^ OPERATOR
        System.out.println(5^6);

        //BINERY NOT ~ OPERATOR
        System.out.println(~1);

        //BINERY LEFT SHIFT <<  OPERATOR
        System.out.println(5<<2);

        //BINERY RIGHT SHIFT >>  OPERATOR
        System.out.println(5>>2);
    }

}